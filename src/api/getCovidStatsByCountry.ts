import axios from "axios";

/* Type for returning object from https://disease.sh/v3/covid-19/countries endpoint */
export type CovidStatsByCountry = {
  updated: number;
  country: string;
  countryInfo: {
    _id: number;
    iso2: string;
    iso3: string;
    lat: number;
    long: number;
    flag: string;
  };
  cases: number;
  todayCases: number;
  deaths: number;
  todayDeaths: number;
  recovered: number;
  todayRecovered: number;
  active: number;
  critical: number;
  casesPerOneMillion: number;
  deathsPerOneMillion: number;
  tests: number;
  testsPerOneMillion: number;
  population: number;
  continent: string;
  oneCasePerPeople: number;
  oneDeathPerPeople: number;
  oneTestPerPeople: number;
  activePerOneMillion: number;
  recoveredPerOneMillion: number;
  criticalPerOneMillion: number;
};

export const getCovidStatsByCountry = () => {
  return axios
    .get(`${process.env.REACT_APP_API_BASE_URL}/countries`)
    .then((res): CovidStatsByCountry[] => res.data);
};
